package com.example.myDCAnimationList.model;

public class LoginDTO {
     private User user;
     private String jwt;

     public LoginDTO() { }

    public LoginDTO(User user, String jwt) {
        this.user = user;
        this.jwt = jwt;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public String getJwt() {
        return jwt;
    }

    public void setJwt(String jwt) {
        this.jwt = jwt;
    }
}
